package oop1115;

public class OperatorTest4 {

	public static void main(String[] args) {
//		연산자 연습
//		대입연산자
		int a = 3;
		a= a + 2;
		System.out.println(a);
		a += 2;
		System.out.println(a);
		a -= 3;
		System.out.println(a);
		a = a*6;
		System.out.println(a);
		a *= 4;
		System.out.println(a);
		a = a / 5;
		System.out.println(a);
		a /= 5;
	}
}
