package oop1115;

public class BitTest {

	public static void main(String[] args) {
//			bit 연산자
//			비트곱     &
//			비트합      |
//			비트부정  ~
/*			
 			128 64 32 16 8 4 2 1
                                  1 0 1 -> 5
                                  1 1 1 -> 7
                               1 1 0 1 -> 13
                       1  0  0  0 1 1 -> 35 
            5 -> 00000101           
            3 -> 00000011          
*/
			System.out.println(5&3);
			System.out.println(5|3);
			
	}
}
